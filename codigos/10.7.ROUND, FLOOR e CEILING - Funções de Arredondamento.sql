-- Funções de arredondamento

-- ROUND
SELECT ROUND(19.76945, 2) -- Arredonda com a quantidade de casas decimais escolhidas

-- ROUND(Truncar)
SELECT ROUND(19.76945, 2, 1) -- Pega casas decimais sem arredondar

-- FLOOR
SELECT FLOOR(19.76945) -- Arredondamento para baixo

-- CEILING
SELECT CEILING(19.76945) -- Arredondamento para cima